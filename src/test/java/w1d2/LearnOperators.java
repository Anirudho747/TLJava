package w1d2;

public class LearnOperators {

    public static void main(String[] args)
    {
        int input1 = 50;
        int input2 = 100;

        System.out.println(input1+input2);
        System.out.println(input2-input1);
        System.out.println(input1*input2);
        System.out.println(input2/input1);
    }
}
